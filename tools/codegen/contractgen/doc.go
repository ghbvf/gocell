// Package contractgen renders typed Go scaffolding from a single contract.yaml +
// its referenced JSON schemas. It is the codegen pass behind
// `gocell generate contract`.
//
// # Pipeline
//
// BuildContractSpec parses the contract metadata + schemaRefs into a
// *ContractGenSpec (spec.go); the spec is then handed to one or more
// templates registered in templates/ via render.go. Each output file lives
// under generated/contracts/<segments>/ where the segments mirror the
// dot-separated contract id (with every "internal" segment rewritten to
// "internalapi" to keep the generated package importable from cells/ and
// examples/ — Go's internal package rule otherwise blocks cross-tree imports;
// see tools/codegen/internal/pathx).
//
// # Artifact matrix
//
// The generator emits a fixed set of files per contract kind. Every file
// carries the "Code generated by gocell generate contract. DO NOT EDIT."
// header so editors and lint suites recognize them as derived artifacts.
//
//	          | types_gen.go | iface_gen.go | handler_gen.go | spec_gen.go | subscription_gen.go
//	----------+--------------+--------------+----------------+-------------+--------------------
//	http      |      ✔       |      ✔       |       ✔        |     —       |          —
//	event     |      ✔       |      ✔       |       —        |     ✔       |          ✔
//	command   |      ✔       |      ✔       |       —        |     —       |          —
//	projection|      ✔       |      ✔       |       —        |     —       |          —
//
// # types_gen.go (every contract kind)
//
// Renders Request / Response / Payload / Headers DTOs from the schemaRefs.
// For HTTP contracts, additionally renders the typed-response-envelope set:
// one {HandlerMethod}ResponseObject interface plus N concrete typed structs,
// one per status declared in contract.yaml http.responses[] union with
// http.successStatus. The generator's Responses IR slice is the single source
// of truth — CH-06 governance enforces the contract.yaml ↔ types_gen.go
// bijection (see kernel/governance/rules_http_typed_envelope.go).
//
// Naming convention for typed response structs:
//
//	{HandlerMethod}{Status}JSONResponse        — success body-bearing (200/201/etc.)
//	{HandlerMethod}{Status}NoContentResponse   — success 204 marker (empty struct)
//	{HandlerMethod}{Status}ErrorResponse       — declared 4xx/5xx, Body errcode.Error
//
// Each typed struct implements an unexported visit{HandlerMethod}Response
// method, closing the implementation set to types declared in this package.
// Service code constructs one of these structs to return; the generated
// handler dispatches via the Visit method, which writes the pinned status +
// body (errors go through pkg/httputil.WriteErrorWithStatus to share the 4xx/
// 5xx redaction policy with the framework fallback path).
//
// # iface_gen.go (every contract kind)
//
// Renders the Service interface that the cell-side handler must implement.
// For HTTP contracts the signature is
// `Method(ctx, *Request) ({HandlerMethod}ResponseObject, error)`: the typed
// response carries declared 2xx/4xx/5xx outcomes; the Go error return is
// reserved for un-declared framework 5xx (panic recover, infrastructure
// faults), in which case the generated handler falls back to
// pkg/httputil.WriteError(err) and derives the response status from
// errcode.Kind. For event contracts the signature is
// `Handle{Topic}(ctx, *Payload) error`.
//
// # handler_gen.go (kind=http only)
//
// Renders the http.Handler that decodes the request, dispatches to Service,
// and writes the typed response. Wires auth.Mount with the contract's
// auth.Public / auth.Bootstrap / auth.PasswordResetExempt flags as declared
// in contract.yaml. Pagination endpoints — every GET that declares cursor +
// limit, regardless of additional path or filter query params — route through
// pkg/httputil.ParsePageParams so the limit error envelope is uniform across
// the entire HTTP surface; this is byte-pinned by the http_order_list_v1
// handler_gen.go golden in render_test.go — any reintroduction of inline
// strconv.ParseInt("limit") in the template diffs the golden output.
// (funnel-first; see docs/plans/202605070431-pr403-funnel-fix-roadmap.md §7.)
//
// # spec_gen.go (kind=event only)
//
// Renders the cell-facing wrapper.ContractSpec literal so cells can
// reference one canonical declaration for the topic, transport, delivery
// semantics, and idempotency policy. Single source for the contract id
// stamped into every consumer's reg.Subscribe call.
//
// # subscription_gen.go (kind=event only)
//
// Renders the typed Subscription registration helper. Cells call
// `{Topic}.Subscribe(reg, handler, consumerGroup)`; the helper resolves the
// canonical ContractSpec from spec_gen.go and asserts the consumerGroup is
// a non-empty string at registration time.
//
// # Cell attribution constraint
//
// Generated *Handler MUST be mounted from a cell-owned RouteGroup
// (typically via the cellgen-generated cell_gen.go reg.RouteGroup wiring).
// http_requests_total `cell` label depends on this:
//
//   - Mounted via reg.RouteGroup → CellAttribution middleware injects
//     kernel/ctxkeys.CellID = <cellID>; metrics labeled cell=<cellID>.
//   - Mounted standalone (e.g. http.Handle) → no CellID context, metrics
//     fall back to the "_runtime" sentinel — silent observability bug.
//
// Reference: .claude/rules/gocell/observability.md "HTTP Metrics `cell` Label".
//
// # Workflow: adding a new declared status code
//
//  1. Edit contracts/<path>/contract.yaml — add the new status code under
//     endpoints.http.responses[] with a schemaRef pointing to the JSON schema
//     for that status's response body (or use the shared error envelope ref for
//     4xx/5xx declared error responses).
//
// 2. Regenerate all typed structs:
//
//		go run ./cmd/gocell generate contract --all
//
//	   This reruns types.tmpl for every HTTP contract, emitting one new
//	   {HandlerMethod}{Status}JSONResponse / NoContentResponse / ErrorResponse
//	   struct for the added status, and updating the {HandlerMethod}ResponseObject
//	   interface to include it.
//
// 3. Validate governance:
//
//		go run ./cmd/gocell check contract-health   # CH-04 + CH-06 must pass
//		golangci-lint run ./...                      # 0 issues
//
//	   CH-06 (rules_http_typed_envelope.go) verifies that the generated struct
//	   set exactly matches contract.yaml SuccessStatus ∪ responses[]; CH-04
//	   (rules_http_response_alignment.go) verifies the handler-emitted status set
//	   is a subset of the declared set. Both must be green before committing.
//
// # Cross-tooling references
//
//   - tools/codegen/internal/pathx — contract id → generated package path
//     (single source of truth for the internal→internalapi rewrite).
//   - tools/codegen/cellgen — cell scaffolding generator (peer pass; shares
//     the codegen.Render plumbing).
//   - tools/archtest/handler_inline_limit_parse_test.go — HANDLER-NO-INLINE-
//     LIMIT-PARSE-01, regression gate against per-param limit parsing.
//   - kernel/governance/rules_http_response_alignment.go — CH-04
//     (handler-emitted status ⊂ contract.yaml.responses[] ∪ auth.responses).
//   - kernel/governance/rules_http_typed_envelope.go — CH-06 (contract.yaml.
//     responses[] ∪ successStatus = generated typed-response struct set).
//
