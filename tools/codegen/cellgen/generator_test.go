func TestRenderSlice_ProducesServiceInterface(t *testing.T) {
	t.Parallel()
	spec := &SliceGenSpec{
		Package: "subs",
		CellID:  "demo",
		SliceID: "subs",
		Handlers: []SliceHandlerSpec{
			{MethodName: "HandleAlpha", ContractID: "event.alpha.v1"},
			{MethodName: "HandleBeta", ContractID: "event.beta.v1"},
		},
	}

	out, err := codegen.Render(codegen.RenderOptions{
		TemplateName: "slice.tmpl",
		Templates:    templates,
		Data:         spec,
		Filename:     "demo/slices/subs/slice_gen.go",
	})
	if err != nil {
		t.Fatalf("render slice: %v", err)
	}
	got := string(out)

	mustContain(t, got, "// Code generated by gocell generate cell. DO NOT EDIT.")
	mustContain(t, got, "package subs")
	mustContain(t, got, "type Service interface {")
	mustContain(t, got, "HandleAlpha(ctx context.Context, e outbox.Entry) outbox.HandleResult")
	mustContain(t, got, "HandleBeta(ctx context.Context, e outbox.Entry) outbox.HandleResult")
	mustContain(t, got, "// HandleAlpha handles event.alpha.v1.")
}

func TestGenerate_DryRunDoesNotWriteFile(t *testing.T) {
	root := initSyntheticRepo(t)
	project := buildSyntheticProject()
	res, err := Generate(root, project, Options{OnlyCell: "demo", DryRun: true})
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}
	if len(res.Generated) != 1 {
		t.Errorf("Generated = %v, want 1 entry", res.Generated)
	}
	if _, err := os.Stat(filepath.Join(root, "cells/demo/cell_gen.go")); !os.IsNotExist(err) {
		t.Errorf("DryRun should not write file; err = %v", err)
	}
}

