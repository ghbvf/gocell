// TestRenderCell_HappyPath_OneListenerOneSubRoute exercises the cell.tmpl
// template via codegen.Render and asserts the rendered Go source contains
// the structural skeleton (package header, interface assertion, RouteGroup,
// initInternal hook, captureErr closure, mux.Route subPath block).
func TestRenderCell_HappyPath_OneListenerOneSubRoute(t *testing.T) {
	t.Parallel()
	spec := &CellGenSpec{
		Package:              "demo",
		StructName:           "Demo",
		CellID:               "demo",
		ConsumerGroupDefault: "demo",
		RouteGroups: []RouteGroupGenSpec{{
			ListenerConst: "cell.PrimaryListener",
			Prefix:        "/api/v1",
			SubRoutes: []RouteSubGroup{{
				SubPath: "/widgets",
				Mounts: []RouteSliceMount{
					{HandlerField: "createH", Method: "RegisterRoutes"},
					{HandlerField: "queryH", Method: "RegisterRoutes"},
				},
			}},
		}},
	}

	out, err := codegen.Render(codegen.RenderOptions{
		TemplateName: "cell.tmpl",
		Templates:    templates,
		Data:         spec,
		Filename:     "demo/cell_gen.go",
	})
	if err != nil {
		t.Fatalf("render: %v", err)
	}
	got := string(out)

	mustContain(t, got, "// Code generated by gocell generate cell. DO NOT EDIT.")
	mustContain(t, got, "package demo")
	mustContain(t, got, "var _ cell.Cell = (*Demo)(nil)")
	mustContain(t, got, "func (c *Demo) Init(ctx context.Context, reg cell.Registry) error {")
	mustContain(t, got, "if err := c.BaseCell.Init(ctx, reg); err != nil {")
	mustContain(t, got, "if err := c.initInternal(ctx, reg); err != nil {")
	mustContain(t, got, "reg.RouteGroup(cell.RouteGroup{")
	mustContain(t, got, "Listener: cell.PrimaryListener,")
	mustContain(t, got, `Prefix:   "/api/v1",`)
	mustContain(t, got, `mux.Route("/widgets", func(s cell.RouteMux) {`)
	mustContain(t, got, "captureErr(c.createH.RegisterRoutes(s))")
	mustContain(t, got, "captureErr(c.queryH.RegisterRoutes(s))")

	// no subscribe path → must NOT import wrapper / fmt
	if strings.Contains(got, `"github.com/ghbvf/gocell/kernel/wrapper"`) {
		t.Errorf("unexpected wrapper import in output without subscriptions:\n%s", got)
	}
}

