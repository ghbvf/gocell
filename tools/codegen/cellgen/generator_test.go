// TestRenderCell_GoldenSynth renders the synthetic project's CellGenSpec
// through cell.tmpl and compares the result against a committed golden file.
// When run with -update it regenerates the golden file instead.
//
// TODO: regenerate golden after template ARCH-02/OPS-06 fixes if the
// shared header template changes again after this golden was written.
func TestRenderCell_GoldenSynth(t *testing.T) {
	t.Parallel()
	spec, err := BuildCellSpec(buildSyntheticProject(), "demo", syntheticBundle())
	if err != nil {
		t.Fatalf("BuildCellSpec: %v", err)
	}
	out, err := codegen.Render(codegen.RenderOptions{
		TemplateName: "cell.tmpl",
		Templates:    templates,
		Data:         spec,
		Filename:     "demo/cell_gen.go",
	})
	if err != nil {
		t.Fatalf("Render: %v", err)
	}

	goldenPath := filepath.Join("testdata", "golden", "synth_cell_gen.go.golden")
	if *updateGolden {
		if err := os.WriteFile(goldenPath, out, 0o644); err != nil {
			t.Fatalf("write golden: %v", err)
		}
		t.Logf("golden file updated: %s", goldenPath)
		return
	}

	golden, err := os.ReadFile(goldenPath) //nolint:gosec // test reads its own committed fixture path
	if err != nil {
		t.Fatalf("read golden %s: %v (run with -update to generate)", goldenPath, err)
	}
	if !bytes.Equal(out, golden) {
		t.Errorf("rendered output diverges from golden:\n--- got ---\n%s\n--- want ---\n%s", out, golden)
	}
}

// TestGenerate_VerifyDriftAfterManualEdit writes cell_gen.go, tampers with
// it by substituting one symbol, then runs Generate in Verify mode. It must
// detect exactly one drifted file. Exercises the write→tamper→verify path
// (TEST-03 / CORRECT-02).
func TestGenerate_VerifyDriftAfterManualEdit(t *testing.T) {
	t.Parallel()
	root := initSyntheticRepo(t)
	project := buildSyntheticProject()
	if _, err := Generate(root, project, Options{OnlyCell: "demo"}); err != nil {
		t.Fatal(err)
	}
	path := filepath.Join(root, "cells", "demo", "cell_gen.go")
	content, err := os.ReadFile(path) //nolint:gosec // test reads its own freshly-written tmp file
	if err != nil {
		t.Fatal(err)
	}
	tampered := strings.Replace(string(content), "Init", "InitX", 1)
	if err := os.WriteFile(path, []byte(tampered), 0o644); err != nil { //nolint:gosec // G306: test writes to its own tmp file with mode 0644
		t.Fatal(err)
	}
	res, err := Generate(root, project, Options{OnlyCell: "demo", Verify: true})
	if err != nil {
		t.Fatal(err)
	}
	if len(res.Drifted) != 1 {
		t.Errorf("expected drift after manual edit, got %v", res.Drifted)
	}
}

// TestProjectFilteredToCell_OnlyTargetCellAndSlices verifies K05-09: the
// helper returns a ProjectMeta containing only the target cell and its slices,
// sharing the contracts map from the original project.
func TestProjectFilteredToCell_OnlyTargetCellAndSlices(t *testing.T) {
	t.Parallel()
	cellA := &metadata.CellMeta{ID: "a", Dir: "a", File: "cells/a/cell.yaml", GoStructName: "A"}
	cellB := &metadata.CellMeta{ID: "b", Dir: "b", File: "cells/b/cell.yaml", GoStructName: "B"}
	sliceA1 := &metadata.SliceMeta{ID: "s1", BelongsToCell: "a", Dir: "s1", File: "cells/a/slices/s1/slice.yaml"}
	sliceB1 := &metadata.SliceMeta{ID: "s1", BelongsToCell: "b", Dir: "s1", File: "cells/b/slices/s1/slice.yaml"}
	contract := &metadata.ContractMeta{ID: "event.foo.v1", Kind: "event"}

	project := &metadata.ProjectMeta{
		Cells:     map[string]*metadata.CellMeta{"a": cellA, "b": cellB},
		Slices:    map[string]*metadata.SliceMeta{"a/s1": sliceA1, "b/s1": sliceB1},
		Contracts: map[string]*metadata.ContractMeta{"event.foo.v1": contract},
	}

	filtered := projectFilteredToCell(project, "a")

	if len(filtered.Cells) != 1 || filtered.Cells["a"] == nil {
		t.Errorf("Cells = %v, want only {a}", filtered.Cells)
	}
	if len(filtered.Slices) != 1 || filtered.Slices["a/s1"] == nil {
		t.Errorf("Slices = %v, want only {a/s1}", filtered.Slices)
	}
	// Contracts map must be the exact same map reference (shared, not deep-copied).
	// Maps cannot be compared with != but we can verify the pointer to a known
	// entry matches.
	if filtered.Contracts["event.foo.v1"] != contract {
		t.Errorf("Contracts should share the same map reference as the original project")
	}
	// Original project must not be mutated.
	if len(project.Cells) != 2 || len(project.Slices) != 2 {
		t.Errorf("original project was mutated")
	}
}

