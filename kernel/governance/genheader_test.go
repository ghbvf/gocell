package governance

import (
	"sort"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

func TestIsGoCellGenerated_AcceptsGoSentinel(t *testing.T) {
	content := []byte(GoGeneratedPrefix + "assembly. DO NOT EDIT.\npackage main\n")
	assert.True(t, IsGoCellGenerated(content))
}

func TestIsGoCellGenerated_AcceptsYAMLSentinel(t *testing.T) {
	content := []byte(YAMLGeneratedPrefix + "metrics-schema. DO NOT EDIT.\nmetrics: {}\n")
	assert.True(t, IsGoCellGenerated(content))
}

func TestIsGoCellGenerated_RejectsPlainGoSource(t *testing.T) {
	content := []byte("package main\n\nfunc main() {}\n")
	assert.False(t, IsGoCellGenerated(content))
}

func TestIsGoCellGenerated_RejectsPlainYAML(t *testing.T) {
	content := []byte("# regular comment\nmetrics: {}\n")
	assert.False(t, IsGoCellGenerated(content))
}

func TestIsGoCellGenerated_RejectsSentinelNotAtPrefix(t *testing.T) {
	// The sentinel must be at the start of the file. Embedding it later
	// (e.g. inside a string literal) must NOT mark the file as generated.
	content := []byte("package main\n\nconst sentinel = \"" + GoGeneratedPrefix + "\"\n")
	assert.False(t, IsGoCellGenerated(content))
}

func TestIsGoCellGenerated_EmptyContent(t *testing.T) {
	assert.False(t, IsGoCellGenerated(nil))
	assert.False(t, IsGoCellGenerated([]byte{}))
}

func TestListGeneratedInHEAD_EmptyForUntrackedDir(t *testing.T) {
	root := t.TempDir()
	got, err := ListGeneratedInHEAD(root)
	require.NoError(t, err)
	assert.Empty(t, got, "non-git directory must return empty list, not error")
}

func TestListGeneratedInHEAD_EmptyForFreshRepo(t *testing.T) {
	root := t.TempDir()
	gitInit(t, root)
	got, err := ListGeneratedInHEAD(root)
	require.NoError(t, err)
	assert.Empty(t, got, "git repo with no HEAD must return empty list, not error")
}

func TestListGeneratedInHEAD_EmptyWhenNoFilesMatch(t *testing.T) {
	root := t.TempDir()
	gitInitAndCommit(t, root, "README.md", "no headers here\n")
	got, err := ListGeneratedInHEAD(root)
	require.NoError(t, err)
	assert.Empty(t, got)
}

func TestListGeneratedInHEAD_FindsGoSentinel(t *testing.T) {
	root := t.TempDir()
	gitInitAndCommit(t, root,
		"cmd/fixture/main.go",
		GoGeneratedPrefix+"assembly. DO NOT EDIT.\npackage main\n")

	got, err := ListGeneratedInHEAD(root)
	require.NoError(t, err)
	assert.Equal(t, []string{"cmd/fixture/main.go"}, got)
}

func TestListGeneratedInHEAD_FindsYAMLSentinel(t *testing.T) {
	root := t.TempDir()
	gitInitAndCommit(t, root,
		"assemblies/fixture/generated/boundary.yaml",
		YAMLGeneratedPrefix+"assembly. DO NOT EDIT.\nassemblyId: fixture\n")

	got, err := ListGeneratedInHEAD(root)
	require.NoError(t, err)
	assert.Equal(t, []string{"assemblies/fixture/generated/boundary.yaml"}, got)
}

func TestListGeneratedInHEAD_IgnoresNonGeneratedSiblings(t *testing.T) {
	root := t.TempDir()
	gitInit(t, root)
	gitConfigUser(t, root)
	writeRepoFile(t, root, "cmd/fixture/main.go",
		GoGeneratedPrefix+"assembly. DO NOT EDIT.\npackage main\n")
	writeRepoFile(t, root, "cmd/fixture/run.go", "package main\n")
	writeRepoFile(t, root, "docs/notes.md", "# regular doc\n")
	gitRun(t, root, "add", ".")
	gitRun(t, root, "commit", "-q", "-m", "fixture", "--no-gpg-sign")

	got, err := ListGeneratedInHEAD(root)
	require.NoError(t, err)
	sort.Strings(got)
	assert.Equal(t, []string{"cmd/fixture/main.go"}, got,
		"hand-written sibling files and unrelated docs must not surface")
}

func TestListGeneratedInHEAD_IgnoresUnsupportedExtensions(t *testing.T) {
	// Files with the sentinel but extensions outside the generator
	// filter (e.g. .json) are intentionally not surfaced — the filter
	// is the gate the future extends together with the generator.
	root := t.TempDir()
	gitInitAndCommit(t, root,
		"data/blob.json",
		"# Generated by gocell generate noop. DO NOT EDIT.\n{}\n")
	got, err := ListGeneratedInHEAD(root)
	require.NoError(t, err)
	assert.Empty(t, got)
}

func TestListGeneratedInHEAD_FindsFilesAtArbitraryDepth(t *testing.T) {
	root := t.TempDir()
	gitInit(t, root)
	gitConfigUser(t, root)
	writeRepoFile(t, root, "internal/generated/rogue.go",
		GoGeneratedPrefix+"experimental. DO NOT EDIT.\npackage generated\n")
	writeRepoFile(t, root, "pkg/x/y/z.yaml",
		YAMLGeneratedPrefix+"deep-nested. DO NOT EDIT.\n")
	gitRun(t, root, "add", ".")
	gitRun(t, root, "commit", "-q", "-m", "fixture", "--no-gpg-sign")

	got, err := ListGeneratedInHEAD(root)
	require.NoError(t, err)
	sort.Strings(got)
	assert.Equal(t, []string{
		"internal/generated/rogue.go",
		"pkg/x/y/z.yaml",
	}, got)
}
