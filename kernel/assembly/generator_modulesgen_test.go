// buildModulesTestProject creates a ProjectMeta with 3 cells that all have
// GoStructName set, suitable for GenerateModulesGen tests.
func buildModulesTestProject() *metadata.ProjectMeta {
	return &metadata.ProjectMeta{
		Cells: map[string]*metadata.CellMeta{
			"accesscore": {
				ID:           "accesscore",
				GoStructName: "AccessCore",
			},
			"auditcore": {
				ID:           "auditcore",
				GoStructName: "AuditCore",
			},
			"configcore": {
				ID:           "configcore",
				GoStructName: "ConfigCore",
			},
		},
		Slices:    make(map[string]*metadata.SliceMeta),
		Contracts: make(map[string]*metadata.ContractMeta),
		Journeys:  make(map[string]*metadata.JourneyMeta),
		Assemblies: map[string]*metadata.AssemblyMeta{
			"corebundle": {
				ID:    "corebundle",
				Cells: []string{"accesscore", "auditcore", "configcore"},
			},
		},
	}
}

// ---------------------------------------------------------------------------
// GenerateModulesGen tests
// ---------------------------------------------------------------------------

func TestGenerateModulesGen_Corebundle(t *testing.T) {
	project := buildModulesTestProject()
	gen := NewGenerator(project, "github.com/ghbvf/gocell", "")

	out, err := gen.GenerateModulesGen("corebundle")
	require.NoError(t, err)

	content := string(out)
	// Generated file marker
	assert.Contains(t, content, "// Code generated by ")
	assert.Contains(t, content, "DO NOT EDIT")
	// Package declaration
	assert.Contains(t, content, "package main")
	// Function signature
	assert.Contains(t, content, "func generatedCellModules() []CellModule")
	// All three module struct literals
	assert.Contains(t, content, "AccessCoreModule{}")
	assert.Contains(t, content, "AuditCoreModule{}")
	assert.Contains(t, content, "ConfigCoreModule{}")
}

func TestGenerateModulesGen_AssemblyNotFound(t *testing.T) {
	project := buildModulesTestProject()
	gen := NewGenerator(project, "github.com/ghbvf/gocell", "")

	_, err := gen.GenerateModulesGen("nonexistent")
	require.Error(t, err)

	var ec *ecErr.Error
	require.True(t, errors.As(err, &ec))
	assert.Equal(t, ecErr.ErrAssemblyNotFound, ec.Code)
}

func TestGenerateModulesGen_CellMissingGoStructName(t *testing.T) {
	project := buildModulesTestProject()
	// Override accesscore to have empty GoStructName
	project.Cells["accesscore"] = &metadata.CellMeta{
		ID:           "accesscore",
		GoStructName: "",
	}
	gen := NewGenerator(project, "github.com/ghbvf/gocell", "")

	_, err := gen.GenerateModulesGen("corebundle")
	require.Error(t, err)

	var ec *ecErr.Error
	require.True(t, errors.As(err, &ec))
	assert.Equal(t, ecErr.ErrMetadataInvalid, ec.Code)
	// Message is a const literal; it must mention GoStructName so the error
	// is self-describing without the Internal field.
	assert.Contains(t, ec.Message, "GoStructName")
}

// TestGenerateModulesGen_UnknownCellRef verifies that when an assembly references
// a cell ID that is not registered in project.Cells, GenerateModulesGen returns
// an errcode.Error with code ErrMetadataInvalid and a message containing "unknown"
// or "not found".
func TestGenerateModulesGen_UnknownCellRef(t *testing.T) {
	project := buildModulesTestProject()
	// Add assembly with an unregistered cell ID.
	project.Assemblies["ghostbundle"] = &metadata.AssemblyMeta{
		ID:    "ghostbundle",
		Cells: []string{"accesscore", "ghost-cell-not-registered"},
	}
	gen := NewGenerator(project, "github.com/ghbvf/gocell", "")

	_, err := gen.GenerateModulesGen("ghostbundle")
	require.Error(t, err)

	var ec *ecErr.Error
	require.True(t, errors.As(err, &ec), "error must be an errcode.Error, got: %T", err)
	assert.Equal(t, ecErr.ErrMetadataInvalid, ec.Code)
	// Message must contain "unknown" or "not found" so the error is self-describing.
	msgLower := strings.ToLower(ec.Message)
	assert.True(t, strings.Contains(msgLower, "unknown") || strings.Contains(msgLower, "not found"),
		"error message should contain 'unknown' or 'not found', got: %q", ec.Message)
}

func TestGenerateModulesGen_PreservesCellsOrder(t *testing.T) {
	project := buildModulesTestProject()
	// Declare assembly with cells in b, a, c order
	project.Cells["b"] = &metadata.CellMeta{ID: "b", GoStructName: "B"}
	project.Cells["a"] = &metadata.CellMeta{ID: "a", GoStructName: "A"}
	project.Cells["c"] = &metadata.CellMeta{ID: "c", GoStructName: "C"}
	project.Assemblies["ordered"] = &metadata.AssemblyMeta{
		ID:    "ordered",
		Cells: []string{"b", "a", "c"},
	}
	gen := NewGenerator(project, "github.com/ghbvf/gocell", "")

	out, err := gen.GenerateModulesGen("ordered")
	require.NoError(t, err)

	content := string(out)
	// B must appear before A, A before C
	posB := indexOfStr(content, "BModule{}")
	posA := indexOfStr(content, "AModule{}")
	posC := indexOfStr(content, "CModule{}")

	assert.Less(t, posB, posA, "BModule must come before AModule")
	assert.Less(t, posA, posC, "AModule must come before CModule")
}

// indexOfStr returns the byte position of substr in s, or -1 if not found.
func indexOfStr(s, substr string) int {
	for i := range len(s) - len(substr) + 1 {
		if s[i:i+len(substr)] == substr {
			return i
		}
	}
	return -1
}
